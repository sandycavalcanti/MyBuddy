package com.example.app_my_buddy;

public class Usuarios {
	private int id, quant_pes_casa, telefone, cpf;
	private String nome, email, senha, descricao, endereco, residencia, status_adocao;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getQuant_pes_casa() {
		return quant_pes_casa;
	}
	public void setQuant_pes_casa(int quant_pes_casa) {
		this.quant_pes_casa = quant_pes_casa;
	}
	public int getTelefone() {
		return telefone;
	}
	public void setTelefone(int telefone) {
		this.telefone = telefone;
	}
	public int getCpf() {
		return cpf;
	}
	public void setCpf(int cpf) {
		this.cpf = cpf;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getSenha() {
		return senha;
	}
	public void setSenha(String senha) {
		this.senha = senha;
	}
	public String getDescricao() {
		return descricao;
	}
	public void setDescricao(String descricao) {
		this.descricao = descricao;
	}
	public String getEndereco() {
		return endereco;
	}
	public void setEndereco(String endereco) {
		this.endereco = endereco;
	}
	public String getResidencia() {
		return residencia;
	}
	public void setResidencia(String residencia) {
		this.residencia = residencia;
	}
	public String getStatus_adocao() {
		return status_adocao;
	}
	public void setStatus_adocao(String status_adocao) {
		this.status_adocao = status_adocao;
	}
		
}